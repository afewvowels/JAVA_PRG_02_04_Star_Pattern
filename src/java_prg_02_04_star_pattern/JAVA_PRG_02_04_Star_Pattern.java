package java_prg_02_04_star_pattern;

/**
 *
 * @author bluebackdev
 * Write a program that displays the following pattern:
 *    *
 *   ***
 *  *****
 * *******
 *  *****
 *   ***
 *    *
 */
public class JAVA_PRG_02_04_Star_Pattern {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        System.out.print("   *\n");
        System.out.print("  ***\n");
        System.out.print(" *****\n");
        System.out.print("*******\n");
        System.out.print(" *****\n");
        System.out.print("  ***\n");
        System.out.println("   *");
    }
    
}
